const isValidJavaIdentifier = (value: string) => {
  if (!value) return false;
  if (JAVA_KEYWORDS.has(value)) return false;
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(value);
};

export { JAVA_KEYWORDS, isValidJavaIdentifier };
